function shuffle(array) {
    let currentIndex = array.length,  randomIndex;
  
    // While there remain elements to shuffle.
    while (currentIndex > 0) {
  
      // Pick a remaining element.
      randomIndex = Math.floor(Math.random() * currentIndex);
      currentIndex--;
  
      // And swap it with the current element.
      [array[currentIndex], array[randomIndex]] = [
        array[randomIndex], array[currentIndex]];
    }
  
    return array;
}

function generateRobot(){
    //called on "generate" from site
    let randomHead = shuffle(head);
    let randomBody = shuffle(body);
    let randomArms = shuffle(arms);
    let randomHands = shuffle(hands);
    let randomLegs = shuffle(legs);
    let randomFlavour = shuffle(flavour);
    let randomAdjectives = shuffle(adjectives);
    let randomNames = shuffle(names);
    let xml = document.implementation.createDocument("", "", null);
    let numRobot = document.getElementById("numRobots").attributes.value;

    var roster = xml.createElement("roster");
    for(i = 0; i <= numRobot; i++){
        var robot = xml.createElement("robot" + i);
        robot.setAttribute("name", (randomAdjectives[i] + " " + randomNames[i]));
        robot.appendChild("flavour", randomFlavour[i]);
        robot.appendChild("head", randomHead[i]);
        robot.appendChild("body", randomBody[i]);
        robot.appendChild("arms", randomArms[i]);
        robot.appendChild("hands", randomHands[i]);
        robot.appendChild("legs", randomLegs[i]);
        xml.appendChild(robot);
    }
}